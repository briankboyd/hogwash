package hogwash.user;

public class User
{
  private int userId;
  private int alias;

  public int getUserId()
  {
    return userId;
  }

  public void setUserId(int userId)
  {
    this.userId = userId;
  }

  public int getAlias()
  {
    return alias;
  }

  public void setAlias(int alias)
  {
    this.alias = alias;
  }

}
